public class assignmentoperator {
    public static void main(String[]args){
        int a=12,b=14,c=12,d=133;
        a+=4;   //12+4
        b-=4;  //i.e 14-4
        b*=4; // i.e 14*4
        d/=11; // i.e 133/11
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
