public class leftshiftexample {
    public static void main(String[]args){
        int a=12;
        System.out.println(a<<2);//12*2^2
        System.out.println(a<<3);//12*2^3
    }
}
/* In Java, the left shift operator (<<) is a binary operator that shifts the bits of a 
number to the left by a specified number of positions.
*/