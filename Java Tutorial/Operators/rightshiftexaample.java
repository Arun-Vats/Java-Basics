public class rightshiftexaample {
    public static void main(String[]args){
        int a=20;
        System.out.println(a>>>2);    //For positive number, >> and >>> works same  
        System.out.println(a>>6);
    }
}
/*The Java right shift operator >> is used to move the value of the left operand 
to right by the number of bits specified by the right operand.

 */